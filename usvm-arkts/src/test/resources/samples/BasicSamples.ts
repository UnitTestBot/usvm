function someOps(a, b) {
    let c = a + b
    let d = c * a
    return d - 1
}

function basicIf(a) {
    let cond = a + 2 - 3
    if (cond > 10) {
        return true
    } else {
        return false
    }
}

function basicArray(arr: number[]) {
    let kek = arr[0]
    if (kek > 5) {
        return true
    } else {
        return false
    }
}
