package org.usvm.samples.arrays;

public class ObjectWithPrimitivesClass {
    public int valueByDefault = 5;

    public int x, y;
    double weight;

    public ObjectWithPrimitivesClass() {
    }

    public ObjectWithPrimitivesClass(int x, int y, double weight) {
        this.x = x;
        this.y = y;
        this.weight = weight;
    }
}
